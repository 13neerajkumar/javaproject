    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        String user_name=jTextField2.getText();
        String password=jTextField3.getText();
        String phn=jTextField4.getText();
        String add=jTextArea1.getText();
        String gender;
        
        if(jRadioButton1.isSelected())
        {
            gender="Male";
        }
        else
        {
            gender="FeMale";
        }

        if( !jLabel10.getText().equals("Already Existed ...!!!!") && !user_name.equals("") && !password.equals("") && !phn.equals("") && !add.equals("") && !gender.equals(""))
        {
            try
            {
                Connection con=joint_login.DB.my_DB();
                PreparedStatement pstmt=con.prepareStatement("insert into std_login values(?,?,?,?,?)");
                pstmt.setString(1,user_name);
                pstmt.setString(2,password);
                pstmt.setString(3,phn);
                pstmt.setString(4,add);
                pstmt.setString(5,gender);

                int flag=pstmt.executeUpdate();

                if(flag>0)
                {
                    JOptionPane.showMessageDialog(null,"Student Added Successfully ...!!!!");
                    jTextField2.setText("");
                    jTextField3.setText("");
                    jTextField4.setText("");
                    jTextArea1.setText("");
                    buttonGroup1.clearSelection();
                    jTextField2.setBackground(new Color(255,255,255));
                }
                else
                {
                    JOptionPane.showMessageDialog(null,"Student NOT Added Successfully ...!!!!");
                }
            }
            catch (Exception e) 
            {
                JOptionPane.showMessageDialog(null,"Error is --> "+e);
            }
        }
        else
        {
            if(jLabel10.getText().equals("Already Existed ...!!!!"))
            {
                JOptionPane.showMessageDialog(null,"Already Existed....!!!! ");
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Please Fill all Values..!! ");
            }
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

