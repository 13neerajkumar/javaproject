    private void jTextField2KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField2KeyReleased
        // TODO add your handling code here:
        
        String user_name=jTextField2.getText();
        
        try
        {
            Connection con=joint_login.DB.my_DB();
            PreparedStatement pstmt=con.prepareStatement("select `s_user_name` from std_login where `s_user_name`=?");
            pstmt.setString(1,user_name);
            
            ResultSet rs=pstmt.executeQuery();
            if(rs.next())
            {
                jTextField2.setBackground(new Color(255,153,153));
                //JOptionPane.showMessageDialog(null,"UserName Already Existed ...!!!!");
                jLabel10.setText("Already Existed ...!!!!");
                //jTextField2.setText("");
                //jTextField2.setBackground(new Color(255,255,255));
            }
            else
            {
                if(user_name.equals(""))
                {
                    jTextField2.setBackground(new Color(255,255,255));
                    jLabel10.setText("");
                }
                else
                {
                    jTextField2.setBackground(new Color(153,204,0));
                    jLabel10.setText("");
                }
            }
        }
        catch (Exception e) 
        {
            JOptionPane.showMessageDialog(null,"Error is --> "+e);
        }
        
    }//GEN-LAST:event_jTextField2KeyReleased

