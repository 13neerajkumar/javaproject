    private void save_in_db(String q_type,String q_level,String player_name,int total_not_attempt,int total_attempt,int valid,int invalid)
    {
        Date dt=new Date();
        DateFormat df=new SimpleDateFormat("dd:MM:YYYY hh:mm:ss");
        
        try 
        {
            Connection con=joint_login.DB.my_DB();
            PreparedStatement pstmt=con.prepareStatement("insert into final_played_game_result values(?,?,?,?,?,?,?,?)");
            pstmt.setString(1,player_name);
            pstmt.setInt(2,total_not_attempt);
            pstmt.setInt(3,total_attempt);
            pstmt.setInt(4,valid);
            pstmt.setInt(5,invalid);
            pstmt.setString(6,df.format(dt));
            pstmt.setString(7,q_type);
            pstmt.setString(8,q_level);
            
            int flag=pstmt.executeUpdate();
            
            if(flag>0)
            {
                JOptionPane.showMessageDialog(null,"save ..!!!");
            }
            else
            {
                JOptionPane.showMessageDialog(null,"NOT save ..!!!");
            }
        }
        catch (Exception e) 
        {
            JOptionPane.showMessageDialog(null,"Error is --> "+e);
        }
    }

